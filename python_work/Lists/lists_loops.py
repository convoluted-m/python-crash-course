# Defining a simple for loop (assigning  a temp variable for each value in the list)
goddesses = ['Mokosz', 'Żywa', 'Ériu', 'Brigid', 'Ceridwen', 'Durga', 'Kali', 'Tara', 'Persephone']
for goddes in goddesses:
    print(goddes)

for goddes in goddesses:
    print(f"Blesssing of the divine {goddes.title()} to you!")
    print(f"May {goddes.title()} guide you on your path.\n")

print("What an abundance of blessings!")



