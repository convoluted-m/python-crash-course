# A Dictionary in a Dictionary

# Define a dict of users where the value associated with each key is an inner dict with userâ€™s first name, last name, and location
users = {
    'aeinstein': {
        'first': 'albert',
        'last': 'einstein',
        'location': 'princeton',
        },
    'mcurie': {
        'first': 'marie',
        'last': 'curie',
        'location': 'paris',
        },
}
# Loop through the dictionary of usernames and the inner dictionary of user info 
# username is the key and the use info is the value
for username, user_info in users.items():
    print(f"\nUsername: {username}")
    full_name = f"{user_info['first']} {user_info['last']}"
    location = user_info['location']
    print(f"\tFull name: {full_name.title()}")
    print(f"\tLocation: {location.title()}")

